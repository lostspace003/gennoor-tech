// Deep Dive PDF
// ═════════════════════════════════════════════════════════════════════

export function generateDeepDivePDF(
  report: any,
  userName: string,
  email: string,
): jsPDF {
  const doc = new jsPDF({ unit: 'mm', format: 'a4' })
  let y = drawHeader(doc, userName, report.score ?? 0)

  // Headline
  y = drawWrappedText(doc, y, report.headline ?? '', {
    fontSize: 15,
    bold: true,
    color: DARK,
  })
  y += 6

  const slides: any[] = report.slides ?? []

  for (const slide of slides) {
    // Section heading from slide title
    y = drawSectionTitle(doc, y, slide.title ?? '')

    // Narration
    if (slide.narration) {
      y = drawWrappedText(doc, y, slide.narration, { fontSize: 9, color: GRAY })
      y += 4
    }

    const content = slide.content
    const type: string = slide.type ?? ''

    switch (type) {
      case 'score': {
        if (content && typeof content.score === 'number') {
          y = ensureSpace(doc, y, 20)
          doc.setFont('helvetica', 'bold')
          doc.setFontSize(28)
          setColor(doc, BLUE)
          doc.text(`${content.score}/100`, PAGE_W / 2, y + 10, { align: 'center' })
          y += 20
          if (content.verdict) {
            y = drawWrappedText(doc, y, content.verdict, { fontSize: 10, color: DARK })
          }
        }
        break
      }

      case 'strength': {
        if (typeof content === 'string') {
          y = drawWrappedText(doc, y, content)
        } else if (content?.statement) {
          y = drawWrappedText(doc, y, content.statement)
        }
        if (content?.highlights && Array.isArray(content.highlights)) {
          for (const h of content.highlights) {
            y = drawWrappedText(doc, y, `• ${h}`, { indent: 4 })
          }
        }
        break
      }

      case 'reality': {
        if (typeof content === 'string') {
          y = drawWrappedText(doc, y, content)
        } else if (content?.realityCheck) {
          y = drawWrappedText(doc, y, content.realityCheck)
          if (content.timeWasted) {
            y += 2
            y = drawWrappedText(doc, y, `Time wasted: ${content.timeWasted}`, {
              bold: true,
              color: ACCENT_AMBER,
            })
          }
        }
        break
      }

      case 'pillars': {
        const pillars = Array.isArray(content) ? content : content?.pillars
        if (Array.isArray(pillars)) {
          for (const p of pillars) {
            y = drawScoreBar(doc, y, p.name, p.score, p.insight)
          }
        }
        break
      }

      case 'monday': {
        if (content) {
          const before: string[] = content.before ?? content.mondayBefore ?? []
          const after: string[] = content.after ?? content.mondayAfter ?? []
          const colW2 = (CONTENT_W - 6) / 2

          y = ensureSpace(doc, y, 10)
          doc.setFont('helvetica', 'bold')
          doc.setFontSize(10)
          setColor(doc, '#ef4444')
          doc.text('Before', MARGIN, y)
          setColor(doc, ACCENT_GREEN)
          doc.text('After', MARGIN + colW2 + 6, y)
          y += 6

          const maxLen = Math.max(before.length, after.length)
          for (let i = 0; i < maxLen; i++) {
            y = ensureSpace(doc, y, LINE_HEIGHT + 2)
            doc.setFont('helvetica', 'normal')
            doc.setFontSize(9)
            setColor(doc, DARK)
            if (before[i]) {
              const bLines = doc.splitTextToSize(`• ${before[i]}`, colW2 - 2)
              let by = y
              for (const bl of bLines) {
                doc.text(bl, MARGIN, by)
                by += LINE_HEIGHT - 1
              }
            }
            if (after[i]) {
              const aLines = doc.splitTextToSize(`• ${after[i]}`, colW2 - 2)
              let ay = y
              for (const al of aLines) {
                doc.text(al, MARGIN + colW2 + 6, ay)
                ay += LINE_HEIGHT - 1
              }
            }
            // Advance y by max lines of either column
            const bCount = before[i] ? doc.splitTextToSize(`• ${before[i]}`, colW2 - 2).length : 0
            const aCount = after[i] ? doc.splitTextToSize(`• ${after[i]}`, colW2 - 2).length : 0
            y += Math.max(bCount, aCount) * (LINE_HEIGHT - 1) + 1
          }
        }
        break
      }

      case 'quickwin': {
        if (content) {
          if (content.title) {
            y = drawWrappedText(doc, y, content.title, { bold: true })
            y += 2
          }
          if (content.timeframe) {
            y = drawWrappedText(doc, y, `Timeframe: ${content.timeframe}`, {
              fontSize: 9,
              color: BLUE,
              bold: true,
            })
          }
          const steps: string[] = content.steps ?? []
          for (let i = 0; i < steps.length; i++) {
            y = drawWrappedText(doc, y, `${i + 1}. ${steps[i]}`, { indent: 4 })
          }
          if (content.expectedResult) {
            y += 2
            y = drawWrappedText(doc, y, `Expected result: ${content.expectedResult}`, {
              fontSize: 9,
              color: ACCENT_GREEN,
              bold: true,
            })
          }
        }
        break
      }

      case 'plan': {
        const planItems: string[] = Array.isArray(content) ? content : content?.steps ?? content?.plan ?? []
        for (const item of planItems) {
          y = ensureSpace(doc, y, LINE_HEIGHT + 4)
          setFillColor(doc, LIGHT_GRAY)
          doc.roundedRect(MARGIN, y - 4, CONTENT_W, LINE_HEIGHT + 4, 2, 2, 'F')
          y = drawWrappedText(doc, y, `• ${item}`, { indent: 4 })
          y += 3
        }
        break
      }

      case 'cta': {
        y = ensureSpace(doc, y, 30)
        setFillColor(doc, LIGHT_GRAY)
        doc.roundedRect(MARGIN, y, CONTENT_W, 24, 3, 3, 'F')
        y += 8
        doc.setFont('helvetica', 'bold')
        doc.setFontSize(11)
        setColor(doc, DARK)
        const ctaText = typeof content === 'string' ? content : 'Ready to accelerate your AI journey?'
        doc.text(ctaText, PAGE_W / 2, y, { align: 'center' })
        y += 7
        doc.setFont('helvetica', 'normal')
        doc.setFontSize(9)
        setColor(doc, BLUE)
        doc.text('Visit gennoor.com or email info@gennoor.com', PAGE_W / 2, y, { align: 'center' })
        y += 14
        break
      }

      default: {
        // Generic: render content as text if it's a string, or stringify
        if (content) {
          const text = typeof content === 'string' ? content : JSON.stringify(content, null, 2)
          y = drawWrappedText(doc, y, text, { fontSize: 9 })
        }
        break
      }
    }

    y += 6
  }

  // Final CTA if no CTA slide
  if (!slides.some((s) => s.type === 'cta')) {
    y = ensureSpace(doc, y, 30)
    setFillColor(doc, LIGHT_GRAY)
    doc.roundedRect(MARGIN, y, CONTENT_W, 24, 3, 3, 'F')
    y += 8
    doc.setFont('helvetica', 'bold')
    doc.setFontSize(11)
    setColor(doc, DARK)
    doc.text('Ready to accelerate your AI journey?', PAGE_W / 2, y, { align: 'center' })
    y += 7
    doc.setFont('helvetica', 'normal')
    doc.setFontSize(9)
    setColor(doc, BLUE)
    doc.text('Visit gennoor.com or email info@gennoor.com', PAGE_W / 2, y, { align: 'center' })
  }

  addFooter(doc)
  return doc
}

